    private void btnPrimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimaActionPerformed
        try {
            String input = txtInput.getText().trim();

        long angka = Long.parseLong(input);
        boolean prima = true;
        if (angka <= 1) prima = false;
        else {
            for (int i = 2; i <= Math.sqrt(angka); i++) {
                if (angka % i == 0) {
                    prima = false;
                    break;
                }
            }
        }

        String hasil = prima ? angka + " adalah bilangan PRIMA."
                             : angka + " bukan bilangan prima.";

        labelOutput.setText(hasil);
        JOptionPane.showMessageDialog(this, hasil, "Hasil Cek", JOptionPane.INFORMATION_MESSAGE);

        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, "Silakan masukkan angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
             txtInput.requestFocus();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnPrimaActionPerformed

