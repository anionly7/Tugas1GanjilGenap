    private void btnGanjilGenapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGanjilGenapActionPerformed
        try {
            String input = txtInput.getText().trim();

        long angka = Long.parseLong(input);
        String hasil;
            if (angka % 2 == 0) {
                hasil = angka + " adalah bilangan GENAP";
            } else {
                hasil = angka + " adalah bilangan GANJIL";
            }


        labelOutput.setText(hasil);
        JOptionPane.showMessageDialog(this, hasil, "Hasil Cek", JOptionPane.INFORMATION_MESSAGE);

        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, "Silakan masukkan angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
             txtInput.requestFocus();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnGanjilGenapActionPerformed

